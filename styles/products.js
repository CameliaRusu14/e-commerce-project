  filterInput.addEventListener("change", (e) => {
    const filterCriteria = e.target.value;
    if (filterCriteria !== "") {
      const filteredProducts = produse.filter((produs) => {
        const nameCondition = produs.name
          .toLocaleLowerCase()
          .includes(filterCriteria.toLocaleLowerCase());
        const priceCondition = produs.price.includes(filterCriteria);
        return nameCondition || priceCondition;
      });
      generateProducts(filteredProducts);
      // console.log(filteredProducts);
    } else {
      generateProducts(produse);
    }
  });
  
  sortingSelection.addEventListener("change", (e) => {
    const sortingValue = e.target.value;
    let sorted;
    if (sortingValue === "low") {
      sorted = produse.sort((a, b) => {
        return a.price > b.price ? -1 : a.price < b.price ? 1 : 0;
      });
    } else {
      sorted = produse.sort((a, b) => {
        return a.price < b.price ? -1 : a.price > b.price ? 1 : 0;
      });
    }
    generateProducts(sorted);
  });
  